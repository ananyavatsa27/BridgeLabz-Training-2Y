import static java.lang.Math.*;

public class StaticImportDemo {
    public static void main(String[] args) {
        System.out.println("Square root of 25: " + sqrt(25));
        System.out.println("2 to the power 4: " + pow(2, 4));
        System.out.println("Max of 8 and 10: " + max(8, 10));
        System.out.println("Min of 8 and 10: " + min(8, 10));
        System.out.println("Absolute value of -50: " + abs(-50));
    }
}
